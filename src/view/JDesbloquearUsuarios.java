    private void carregarUsuariosBloqueados() {
    javax.swing.table.DefaultTableModel modelo = 
        (javax.swing.table.DefaultTableModel) jtabelaUsuarios.getModel();
    
    modelo.setRowCount(0); 

    UsuarioController controller = new UsuarioController();
    java.util.List<model.McadUsuario> lista = controller.buscarUsuariosBloqueados();

    for (model.McadUsuario u : lista) {
        modelo.addRow(new Object[] {
            u.getId(),
            u.getNome(),
            u.getLogin(),
            u.getFuncao()
        });
    }
}

