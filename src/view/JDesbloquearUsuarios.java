    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
          int linhaSelecionada = jtabelaUsuarios.getSelectedRow();

         if (linhaSelecionada >= 0) {
    int idUsuario = (int) jtabelaUsuarios.getValueAt(linhaSelecionada, 0);
    UsuarioController controller = new UsuarioController();
    controller.desbloquearUsuario(idUsuario);
    carregarUsuariosBloqueados(); 
    } else {
    JOptionPane.showMessageDialog(this, "Selecione um usuário para desbloquear.");
    }
    }//GEN-LAST:event_jButton2ActionPerformed

