    private void btnToggleSenhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnToggleSenhaActionPerformed
         if (senhaVisivel) {
        txtSenha.setEchoChar('\u2022'); 
        btnToggleSenha.setIcon(new ImageIcon(getClass().getResource("/imagens/olho_fechado.png")));
        senhaVisivel = false;
    } else {
        txtSenha.setEchoChar((char) 0); 
        btnToggleSenha.setIcon(new ImageIcon(getClass().getResource("/imagens/olho_aberto.png")));
        senhaVisivel = true;
    }
    }//GEN-LAST:event_btnToggleSenhaActionPerformed

