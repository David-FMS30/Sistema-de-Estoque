    private void btEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEntrarActionPerformed
          String login = txtLogin.getText();
          String senha = String.valueOf(txtSenha.getPassword());

    if (login.isEmpty() || senha.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Preencha todos os campos.");
        return;
    }
    
    conexao.UsuarioDAO dao = new conexao.UsuarioDAO();
    McadUsuario usuarioLogado = dao.autenticar(login, senha);

    if (usuarioLogado != null) {
        JOptionPane.showMessageDialog(this,
            "Bem-vindo, " + usuarioLogado.getNome() + " (" + usuarioLogado.getFuncao() + ")");
        new JPrincipal(usuarioLogado).setVisible(true);
        dispose();
    } else {
        JOptionPane.showMessageDialog(this, "Usuário ou senha incorretos ou conta bloqueada.");
    }
    }//GEN-LAST:event_btEntrarActionPerformed

    private void bSairActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSairActionPerformed
        int resposta = JOptionPane.showConfirmDialog(
            null,
            "Tem certeza que deseja sair?",
            "Confirmar Saída",
            JOptionPane.YES_NO_OPTION);

    if (resposta == JOptionPane.YES_OPTION) {
        System.exit(0);
    } else {
        JOptionPane.showMessageDialog(null, "Saída cancelada."); 
    }
    }//GEN-LAST:event_bSairActionPerformed

